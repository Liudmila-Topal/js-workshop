/**
 * Pseudocode for printing first n numbers using a for loop
 * ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
 * START
 *   INPUT n
 *
 *   // Using a FOR loop
 *   FOR counter FROM 1 TO n DO
 *     PRINT counter
 *   END FOR
 * END
 */

const limit = 50;
for (let counter = 0; counter <= limit; counter++){
    console.log(counter);
}