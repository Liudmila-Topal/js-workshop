/*
Example 1:
In this example, we will perform basic division on Numbers.
 */
console.log(100 / 20);
console.log(Infinity / 0);
console.log(Infinity / -0);

/*
Example 2:
In this example, we will perform a division operation on a string/non-numbers.
 */
console.log("100" / 20);
console.log("Hello" / 0); // The string is converted to its corresponding number type and the string which does not contain any number is converted to NaN.