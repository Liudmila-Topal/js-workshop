/*
Example 1:
In this example, we will see the value returned when the increment operator is applied after the operand (postfix increment).
 */
let x = 10;
console.log(x++);
console.log(x); // We can observe that first the value is returned and then incremented.

/*
Example 2:
In this example, we will see the values returned when the operator is applied before the operand (prefix increment).
 */
let a = 10;
console.log(++a);
console.log(a); // Here the value is first incremented and then returned.