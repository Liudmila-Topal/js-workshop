/*
Example 1:
In this example, we will see the value returned when the decrement operator is applied after the operand (postfix decrement).
 */
let x = 10;
console.log(x--);
console.log(x); // We can observe that first the value is returned and then decremented.

/*
Example 2:
In this example, we will see the values returned when the operator is applied before the operand (prefix decrement).
 */
let a = 10;
console.log(--a);
console.log(a); // Here the value is first decremented and then returned.