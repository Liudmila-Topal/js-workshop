/*
Example 1:
In this example, we will perform multiplication on Number.
 */
console.log(100 * 20);
console.log(Infinity * 100);
console.log(Infinity * 0);
console.log(NaN * 100);

/*
Example 2:
In this example, we will perform multiplication on string/non-numbers.
 */
console.log("Hello" * 20); // The string is converted to its corresponding number type and the string which does not contain any number is converted to NaN
console.log("100" * 100);