/*
Example 1:
This example shows the use of JavaScript Unary negation(-) Operator.
 */
const a = 20;
const b = -a;

console.log(b);
console.log(typeof b);

const x = '20';
const y = -x;

console.log(y);
console.log(typeof y);

/*
Example 2:
This example shows the use of JavaScript Unary negation(-) Operator while working with numbers.
 */
const c = 30;
const d = -x;

console.log(c);
console.log(typeof d);

/*
Example 3:
This example shows the use of JavaScript Unary negation(-) Operator working with non-numbers.
 */
const e = "30";
const f = -e;

console.log(f);
console.log(typeof f);